 /*for open nav*/
 function openNav(){
	document.getElementById('slide-navbar').style.width="100%";
	document.getElementById('down-slidemenu').style.display="block";
	document.getElementById('button').style.display="block";
}

 /*for close nav*/
function closeNav(){
	document.getElementById('slide-navbar').style.width=0;
	document.getElementById('down-slidemenu').style.display="none";
	document.getElementById('button').style.display="none";
}


